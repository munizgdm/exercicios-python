#13. Desenvolva um algoritmo que receba a base e a altura de um retângulo, calcule e mostre a área desse retângulo. Fórmula da área de um retângulo: A = base . altura.

base = int(input('Informe a base do triangulo: '))
altura = int(input('Informe a altura do triangulo: '))
area = base * altura
print('A área do a triangulo é: {}'.format(area))
