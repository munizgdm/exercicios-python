#22. Desenvolva um algoritmo que receba dois valores numérico inteiro, calcule e mostre a soma do quadrado desses dois números.

num1 = int(input('Informe um número: '))
num2 = int(input('Informe outro número: '))

quadrado = (num1**2) + (num2**2)

print('A soma dos valores elevado ao quadrado: {}'.format(quadrado))
