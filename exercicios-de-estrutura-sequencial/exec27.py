#19. Desenvolva um algoritmo que receba o raio (R) de uma circunferência, calcule e mostre o comprimento dessa circunferência. 
#Fórmula do comprimento da circunferência: C = 2 . π . R.

raio = int(input('Informe o RAIO de uma circunferência: '))

comprimento = 2 * 3.14 * raio

print('O comprimento da circunferência é: {}'.format(comprimento))
